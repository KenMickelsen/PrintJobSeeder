@app.route('/api/emr/start-print-run', methods=['POST'])
def emr_start_print_run():
    """Start a bulk healthcare print run. Returns session_id for SSE stream."""
    num_jobs = int(request.form.get('num_jobs', 10))
    timing_mode = request.form.get('timing_mode', 'random')
    fixed_delay = float(request.form.get('fixed_delay', 1.0))
    min_delay = float(request.form.get('min_delay', 0.5))
    max_delay = float(request.form.get('max_delay', 30.0))

    settings = load_settings()
    destination = settings.get('destination', 'cloud_link')

    # If a specific printer was passed (e.g. from patient/dashboard print buttons), use it directly
    specific_printer = request.form.get('printer', '').strip()
    path_filter = settings.get('industry_paths', {}).get(INDUSTRY, '*Healthcare*')

    if destination == 'on_premise':
        url = build_onprem_url(settings)
        raw_token = settings['on_premise'].get('bearer_token', '')
        bearer_token = f'Bearer {raw_token}' if raw_token and not raw_token.startswith('Bearer ') else raw_token
        if specific_printer:
            printers = [specific_printer]
        else:
            raw_key = deobfuscate_key(settings['cloud_link'].get('api_key', ''))
            region = settings['cloud_link'].get('region', '')
            printers, _ = fetch_printers_from_api(raw_key, get_cloud_base_url(region), path_filter) if (region and raw_key) else ([], None)
    else:
        region = settings['cloud_link'].get('region', '')
        raw_key = deobfuscate_key(settings['cloud_link'].get('api_key', ''))
        if not region or not raw_key:
            return jsonify({'success': False, 'error': 'Cloud Link not configured'}), 400
        base = get_cloud_base_url(region)
        url = f'{base}/v1/print'
        bearer_token = f'Bearer {raw_key}'
        if specific_printer:
            printers = [specific_printer]
        else:
            printers, fetch_error = fetch_printers_from_api(raw_key, base, path_filter)
            if not printers:
                return jsonify({'success': False, 'error': fetch_error or 'No printers found in that folder'}), 400

    if not url:
        return jsonify({'success': False, 'error': 'Print destination not configured'}), 400

    session_id = str(uuid.uuid4())
    job_sessions[session_id] = {
        'status': 'running',
        'results': [],
        'stop_flag': False,
        'total': num_jobs,
        'completed': 0,
        'success_count': 0,
    }

    thread = threading.Thread(
        target=_run_print_jobs,
        args=(session_id, url, bearer_token, num_jobs,
              timing_mode, fixed_delay, min_delay, max_delay, printers),
        daemon=True
    )
    thread.start()
    log(f"EMR print run started: session={session_id}, jobs={num_jobs}, printers={len(printers)}")
    return jsonify({'success': True, 'session_id': session_id, 'printer_count': len(printers)})


def _run_print_jobs(session_id, url, bearer_token, num_jobs,
                    timing_mode, fixed_delay, min_delay, max_delay, printers):
    """Worker thread: sends print jobs and updates session state."""
    import time
    session = job_sessions[session_id]
    filenames = INDUSTRY_PRESETS.get(INDUSTRY, INDUSTRY_PRESETS['healthcare'])
    usernames = USERNAME_PRESETS.get(INDUSTRY, USERNAME_PRESETS['healthcare'])

    for i in range(1, num_jobs + 1):
        if session['stop_flag']:
            break

        filename = filenames[(i - 1) % len(filenames)]
        username = usernames[(i - 1) % len(usernames)]
        printer = printers[(i - 1) % len(printers)]

        buffer = generate_pdf(filename, INDUSTRY, min_pages=1, max_pages=8)
        result = send_single_job_from_buffer(
            url, bearer_token, buffer, filename, username, printer, i, INDUSTRY
        )

        session['results'].append(result)
        session['completed'] = i
        if result['success']:
            session['success_count'] += 1

        if i < num_jobs and not session['stop_flag']:
            delay = generate_random_delay(timing_mode, fixed_delay, min_delay, max_delay)
            time.sleep(delay)

    session['status'] = 'stopped' if session['stop_flag'] else 'complete'
    log(f"EMR print run {session_id}: {session['success_count']}/{session['completed']} succeeded")


@app.route('/api/emr/stream-jobs/<session_id>')
def emr_stream_jobs(session_id):
    """SSE endpoint: streams job results to the browser."""
    import time
    import json as _json

    def generate():
        last_sent = 0
        while True:
            session = job_sessions.get(session_id)
            if not session:
                yield 'data: {"error": "Session not found"}\n\n'
                break

            results = session['results']
            for result in results[last_sent:]:
                yield f'data: {_json.dumps(result)}\n\n'
                last_sent += 1

            if session['status'] in ('complete', 'stopped') and last_sent >= len(results):
                summary = {
                    'type': 'complete',
                    'total': session['completed'],
                    'success': session['success_count'],
                    'status': session['status']
                }
                yield f'data: {_json.dumps(summary)}\n\n'
                break

            time.sleep(0.5)

    return Response(generate(), mimetype='text/event-stream',
                    headers={'Cache-Control': 'no-cache', 'X-Accel-Buffering': 'no'})


@app.route('/api/emr/stop-jobs/<session_id>', methods=['POST'])
def emr_stop_jobs(session_id):
    session = job_sessions.get(session_id)
    if session:
        session['stop_flag'] = True
        return jsonify({'success': True})
    return jsonify({'success': False, 'error': 'Session not found'}), 404


@app.route('/api/emr/session-status/<session_id>')
def emr_session_status(session_id):
    session = job_sessions.get(session_id)
    if not session:
        return jsonify({'error': 'Session not found'}), 404
    return jsonify({
        'status': session['status'],
        'completed': session['completed'],
        'total': session['total'],
        'success_count': session['success_count'],
    })


