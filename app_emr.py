@app.route('/api/emr/print-order', methods=['POST'])
def emr_print_order():
    """Print a single clinical document as a healthcare PDF."""
    data = request.get_json(force=True)
    order_id = data.get('order_id', 'ENC-00000')
    printer = data.get('printer', '')
    username = data.get('username', 'emr-system@meridianhealth.org')

    if not printer:
        return jsonify({'success': False, 'error': 'No printer specified'}), 400

    url, token = _get_print_url_and_token()
    if not url:
        return jsonify({'success': False, 'error': 'Print destination not configured'}), 400

    filename = f'{order_id}_Document.pdf'
    buffer = generate_pdf(filename, INDUSTRY, min_pages=1, max_pages=3)
    result = send_single_job_from_buffer(url, token, buffer, filename, username, printer, 1, INDUSTRY)

    return jsonify({
        'success': result['success'],
        'order_id': order_id,
        'status_code': result['status_code'],
        'message': 'Print job sent successfully' if result['success'] else result['response']
    })


